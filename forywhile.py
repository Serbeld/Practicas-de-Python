#Programa los numeros del 1 al 10 con ciclo for y luego con ciclo while

print("__________________________________________________________________________________________________")

for num in range(1,11):
	print("la cuenta for va en: ",num)

print("__________________________________________________________________________________________________")

num=0

while num < 10:
	num += 1
	print("la cuenta while va en: ",num)

print("__________________________________________________________________________________________________")